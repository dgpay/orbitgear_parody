        <h3>MORE ABOUT THE LIMITED LIFETIME WARRANTY</h3>
        <p><b>What does the limited lifetime warranty mean?</b></p>
        <p>The limited lifetime warranty refers to the length of time, through normal use, a ORBIT Modular Gear & Carry Goods product will function for its intended purpose. It does not apply to the lifetime of the user.</p>
        <p><b>What does general wear mean?</b></p>
        <p>General wear is the expected, organic decomposition of leather or fabric materials over a sustained period of regular usage. Use of any ORBIT Modular Gear & Carry Goods item outside reasonable expected use voids the limited lifetime warranty.</p>
        <p><b>What qualifies an item for repair or replacement?</b></p>
        <p>Mending an existing item is most often our preferred course of action. However, on a case-by-case basis, an item may be deemed beyond the scope of normal repair and our craftspeople may decide to replace it. If you find your ORBIT Modular Gear & Carry Goods item too treasured to part with and a replacement item is unwanted, we will happily repair it to the best of our ability. In these cases, please be aware that, if a substantial amount of labor is involved, some repairs may incur a charge. Moreover, if a defective item results in replacement, we are unable to return the original product.</p>
