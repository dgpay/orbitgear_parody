        <h3>OUR STORY</h3>
        <p>ORBITGear is a Jakarta based technical gear and garment maker.</p>
        <p>Founded in 2008, ORBITGear modern history begins in 2017 with a decade of experiences in carrygoods & technical gear making.</p>
        <p>We believe in the higher value than just creating a product that works. We believe that at ORBITGear,everyone needs to be happy and proud of what  they’re capable of doing and their contribution.
        We believe that a happy worker can only make a better and responsible product with integrity, down to the smallest details. That’s why we decided to run ORBITGear independently and only made all the products in-house.</p>
        <p>Being obsessed with functional details and technical design, ORBITGear seek to provide a practical solution to the everyday commuting problem.</p>
        <p>Inspired by sportswear versatility, military gear practicality, and mountaineering gear functionality, every piece of ORBITGear designed heavily focused on functional details and practical features with a tailored fit for better experiences as daily wear..</p>
